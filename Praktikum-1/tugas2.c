#include <stdio.h>

int main()
{
    // untuk menyimpan angka panjang tipe data nya apa
    long angka = 2147483648;
    printf("Hello, World!\n");
    printf("Nilai angka: %ld\n", angka);

    // untuk menyimpan nomor 12 digit tipe data nya apa
    char nomor[] = "012345678999";
    printf("Nilai nomor: %s\n", nomor);
    return 0;
}